/**
 * @swagger
 * /api/products:
 *   post:
 *     summary: Create a new product (Admin only)
 *     tags: [Products]
 *     security:
 *       - bearerAuth: []
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - name
 *               - price
 *               - category
 *               - stock
 *             properties:
 *               name:
 *                 type: string
 *                 description: Product name
 *                 example: "iPhone 15 Pro"
 *               description:
 *                 type: string
 *                 description: Product description
 *                 example: "Latest iPhone with advanced features"
 *               price:
 *                 type: number
 *                 description: Product price
 *                 example: 999.99
 *               originalPrice:
 *                 type: number
 *                 description: Original price before discount
 *                 example: 1099.99
 *               image:
 *                 type: string
 *                 description: Product image URL
 *                 example: "https://example.com/iphone15.jpg"
 *               category:
 *                 type: string
 *                 description: Product category
 *                 example: "Electronics"
 *               brand:
 *                 type: string
 *                 description: Product brand
 *                 example: "Apple"
 *               stock:
 *                 type: number
 *                 description: Available stock quantity
 *                 example: 50
 *               rating:
 *                 type: number
 *                 description: Average rating (1-5)
 *                 example: 4.5
 *               reviews:
 *                 type: number
 *                 description: Number of reviews
 *                 example: 120
 *               isOnSale:
 *                 type: boolean
 *                 description: Whether product is on sale
 *                 example: true
 *               isFeatured:
 *                 type: boolean
 *                 description: Whether product is featured
 *                 example: false
 *               tags:
 *                 type: array
 *                 items:
 *                   type: string
 *                 description: Product tags
 *                 example: ["smartphone", "5G", "camera"]
 *     responses:
 *       201:
 *         description: Product created successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 success:
 *                   type: boolean
 *                   example: true
 *                 data:
 *                   $ref: '#/components/schemas/Product'
 *       400:
 *         description: Invalid input
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 *       401:
 *         description: Not authorized
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 *       403:
 *         description: Admin access required
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 */
router.post('/', protect, authorize('admin'), createProduct);

